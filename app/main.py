def battle(knights_config: dict) -> dict:
    lancelot = Knight(knights_config["lancelot"]).apply_extension()
    arthur = Knight(knights_config["arthur"]).apply_extension()
    mordred = Knight(knights_config["mordred"]).apply_extension()
    red_knight = Knight(knights_config["red_knight"]).apply_extension()

    def fight(knight1: Knight, knight2: Knight) -> None:
        knight1.hp -= knight2.power - knight1.protection
        knight2.hp -= knight1.power - knight2.protection
        if knight1.hp <= 0:
            knight1.hp = 0

        if knight2.hp <= 0:
            knight2.hp = 0

    fight(lancelot, mordred)
    fight(arthur, red_knight)

    return {
        lancelot.name: lancelot.hp,
        arthur.name: arthur.hp,
        mordred.name: mordred.hp,
        red_knight.name: red_knight.hp
    }


