def battle(knights: dict) -> dict:
    list_knight = []
    for knight in knights:
        knight = KNIGHT(name=knights[knight]["name"],
                        power=knights[knight]["power"],
                        hp=knights[knight]["hp"],
                        armor=knights[knight]["armour"],
                        weapon=knights[knight]["weapon"],
                        potion=knights[knight]["potion"])
        list_knight.append(knight)

    lancelot = list_knight[0]
    arthur = list_knight[1]
    mordred = list_knight[2]
    red_knight = list_knight[3]

    lancelot.fight(mordred)
    mordred.fight(lancelot)
    arthur.fight(red_knight)
    red_knight.fight(arthur)

    return {
        lancelot.name: lancelot.hp,
        arthur.name: arthur.hp,
        mordred.name: mordred.hp,
        red_knight.name: red_knight.hp,
    }
