def battle(knights: dict) -> dict:
    knight_dict = {}
    for knight_confiq in knights.values():
        knight_object = Knight(
            name=knight_confiq["name"],
            power=knight_confiq["power"],
            hp=knight_confiq["hp"]
        )
        knight_object.get_power(knight_confiq)
        knight_object.get_hp(knight_confiq)
        knight_object.get_protection(knight_confiq)
        knight_dict[knight_object.name] = knight_object

    fight_knights(
        knight_dict["Lancelot"],
        knight_dict["Mordred"]
    )

    fight_knights(
        knight_dict["Arthur"],
        knight_dict["Red Knight"]
    )
    return {
        key: value.hp for key, value in knight_dict.items()
    }
