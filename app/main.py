def battle(knights_stats: dict) -> dict:
    knights_dict = {
        name: Knight.create_knight_from_dict(stats)
        for name, stats in knights_stats.items()
    }

    for knight in knights_dict.values():
        knight.prepare_for_battle()

    knights_dict["lancelot"].fight(knights_dict["mordred"])
    knights_dict["arthur"].fight(knights_dict["red_knight"])

    Knight.final_result(knights_dict)

    return {knight.name: knight.hp for knight in knights_dict.values()}


