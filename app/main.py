def battle(knightsConfig: dict) -> dict: # noqa

    # CREATING KNIGHTS
    knights = {
        name: Knight(*attributes.values())
        for name, attributes in knightsConfig.items()
    }

    # BATTLE PREPARATIONS:
    for knight in knights.values():
        applied_equipments(knight)

    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred ---> check if someone fell in battle:
    battle_begin(knights["lancelot"], knights["mordred"])

    # 2 Arthur vs Red Knight ---> check if someone fell in battle:
    battle_begin(knights["arthur"], knights["red_knight"])

    # Return battle results:
    return battle_result(knights)


