from app.Knight import Knight


def check_hp(hp: int) -> int:
    if hp <= 0:
        return 0
    return hp


def fight(knight1: Knight, knight2: Knight) -> None:
    knight1.hp = check_hp(knight1.hp - knight2.power + knight1.protection)
    knight2.hp = check_hp(knight2.hp - knight1.power + knight2.protection)


def battle(participants: list) -> dict:
    knights = []
    for person in participants:
        temp = Knight(participants[person])
        knights.append(temp)

    fight(knights[0], knights[2])
    fight(knights[1], knights[3])
    return {knights[i].name: knights[i].hp for i in range(len(knights))}


