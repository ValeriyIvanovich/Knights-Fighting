def battle(knightsconfig: dict) -> dict:
    for knight in knightsconfig.values():
        Knight.knights.append(Knight(name=knight["name"],
                              power=knight["power"],
                              hp=knight["hp"],
                              armour=knight["armour"],
                              weapon=knight["weapon"],
                              potion=knight["potion"]))

    for knight in Knight.knights:
        knight.equip_armour()
        knight.equip_weapon()
        knight.use_potion()

    Knight.battle("Lancelot", "Mordred")
    Knight.battle("Artur", "Red Knight")

    return {knight.name: knight.hp for knight in Knight.knights}


