def tournament(knight1: Knights, knight2: Knights) -> None:
    knight1.hp -= knight2.power - knight1.protection
    knight2.hp -= knight1.power - knight2.protection

    if knight1.hp <= 0:
        knight1.hp = 0

    if knight2.hp <= 0:
        knight2.hp = 0


def battle(knights_config: dict) -> dict:
    knights = {
        name: Knights(knights_config[name])
        for name in knights_config
    }

    for knight in knights.values():
        knight.prepare_to_battle()

    tournament(knights["lancelot"], knights["mordred"])
    tournament(knights["arthur"], knights["red_knight"])

    return {
        knight.name: knight.hp
        for knight in knights.values()
    }


