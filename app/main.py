def battle(knights: dict) -> dict:
    # BATTLE PREPARATIONS:
    knights_prep = knights_preparing(knights)

    # BATTLE:
    for i in range(int(len(knights_prep) / 2)):
        knights_prep[i].battle(knights_prep[i + 2])
        print(f"{knights_prep[i].name} fight vs {knights_prep[i + 2].name}")
        if knights_prep[i].hp > knights_prep[i + 2].hp:
            print(f"{knights_prep[i].name} win!")
        elif knights_prep[i].hp < knights_prep[i + 2].hp:
            print(f"{knights_prep[i +2].name} win!")
        else:
            print("Draw")

    # Return battle results:
    return {knights_prep[i].name: knights_prep[i].hp
            for i in range(len(knights_prep))}


